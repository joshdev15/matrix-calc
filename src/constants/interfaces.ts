/** IResult interface is used to define the result object
 * with system, x and y variables
 */
export interface IResult2x2 {
  system: number;
  x: number;
  y: number;
}

/** IResult interface is used to define the result object
 * with system, x, y and z variables
 */
export interface IResult3x3 {
  system: number;
  x: number;
  y: number;
  z: number;
}
